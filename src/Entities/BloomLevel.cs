namespace Databank.Entities;

/// <summary>
/// Revised Bloom's Taxonomy classification for cognitive learning objectives
/// Based on Krathwohl (2002)
/// </summary>
public enum BloomLevel
{
    Remember = 1,    // Recall facts and basic concepts
    Understand = 2,  // Explain ideas or concepts
    Apply = 3,       // Use information in a new situation
    Analyze = 4,     // Draw connections among ideas
    Evaluate = 5,    // Justify a stance or decision
    Create = 6       // Produce new or original work
}
